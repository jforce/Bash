PROCURAR () {
IFS=$'\n'
for CODE in $IMDB; do
lynx -connect_timeout=10 --source "http://api.themoviedb.org/2.1/Movie.imdbLookup/pt/xml/2139fe2ff8e32e6081db53edaf2b18af/$CODE" > lixo
TMDB=$(cat lixo | grep "<id>" | sed -e :a -e 's/<[^>]*>//g;s/ //g;s/\t//g')
TITULO=$(cat lixo | grep "<original_name>" | sed -e :a -e 's/<[^>]*>//g;s/\t//g;s/  //g')
if [[ $TMDB != "" ]]; then
echo -e "$TITULO"
echo -e "$CODE -> $TMDB"
echo -e ""
rm lixo
fi
done
}

